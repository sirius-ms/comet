package de.unijena.bioinf.babelms.inputresource;

import java.io.ByteArrayInputStream;
import java.io.InputStream;

public class ByteInputResource implements InputResource<byte[]> {
    private final byte[] bytes;
    private final String name;

    public ByteInputResource(byte[] bytes, String name) {
        this.bytes = bytes;
        this.name = name;
    }

    @Override
    public byte[] getResource() {
        return bytes;
    }

    @Override
    public byte[] getBytes() {
        return getResource();
    }

    @Override
    public InputStream getInputStream() {
        return new ByteArrayInputStream(getResource());
    }

    @Override
    public String getFilename() {
        return name;
    }


    @Override
    public boolean isEmpty() {
        return getSize() == 0;
    }

    @Override
    public long getSize() {
        return getResource().length;
    }
}

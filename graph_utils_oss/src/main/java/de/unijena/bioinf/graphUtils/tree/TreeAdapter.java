package de.unijena.bioinf.graphUtils.tree;

import java.util.List;

/**
 * This interface defines functions to get access to tree nodes. You can connect each tree implementation
 * with this tree framework by writing an adapter class for it - without changing your original tree
 * implementation.
 * Nevertheless: Your tree class could also implement the TreeType interface and use the
 * TreeType.Adapter class.
 * @param <T>
 */
public interface TreeAdapter<T> {

    /**
     * In the most cases, this should be children().size(). Nevertheless, some
     * trees maybe have a faster access to the number of children than to the children itself,
     * so this method should be called if you just want the children number.
     * @param vertex
     * @return number of children of this de.unijena.bioinf.tree node
     */
    public int getDegreeOf(T vertex);

    /**
     * @return (maybe immutable) list of children of this de.unijena.bioinf.tree node
     */
    public List<T> getChildrenOf(T vertex);

}

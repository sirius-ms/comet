package de.unijena.bioinf.graphUtils.tree;

import java.util.List;

public interface BackrefTreeType<T extends BackrefTreeType<T>> extends TreeType<T> {

    public T getParent();
    public int getDepth();
    public int indexOfSibling();

    public static class Adapter<T extends BackrefTreeType<T>> implements BackrefTreeAdapter<T> {

        @Override
        public T getParent(T node) {
            return node.getParent();
        }

        @Override
        public int indexOfSibling(T node) {
            return node.indexOfSibling();
        }

        @Override
        public int getDepth(T node) {
            return node.getDepth();
        }

        @Override
        public int getDegreeOf(T vertex) {
            return vertex.degree();
        }

        @Override
        public List<T> getChildrenOf(T vertex) {
            return vertex.children();
        }
    }

}

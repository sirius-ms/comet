package de.unijena.bioinf.graphUtils.tree;

import java.util.ArrayList;
import java.util.List;

/**
 * @author Kai Dührkop
 */
public class Tree<T> implements TreeType<Tree<T>> {

    private final ArrayList<Tree<T>> children;
    private final T label;

    public Tree(T label) {
        this.children = new ArrayList<Tree<T>>();
        this.label = label;
    }

    public T getLabel() {
        return label;
    }

    public void addChild(Tree<T> children) {
        this.children.add(children);
    }

    public void removeChild(T child) {
        this.children.remove(child);
    }

    public TreeCursor<Tree<T>> getCursor() {
        return StackedTreeCursor.create(this);
    }

    @Override
    public int degree() {
        return children.size();
    }

    @Override
    public List<Tree<T>> children() {
        return children;
    }
}
